    public void saleProductList()
    {
        float total=0;
        try
        {
        Class.forName("com.mysql.jdbc.Driver");
        con=DriverManager.getConnection(url,user,pass);
        pst=con.prepareStatement("select p.pid,p.Brand,p.pname,p.QType,p.price,p.discount,p.finalPrice,s.totalquantity,s.totalprice from Product p join tempsaleproduct s on p.pid=s.pid");
        rs=pst.executeQuery();
        ResultSetMetaData rsmd=rs.getMetaData();
        int q=rsmd.getColumnCount();
        DefaultTableModel dtm=(DefaultTableModel)jTableTest.getModel();
        dtm.setRowCount(0);
        while(rs.next())
        {
            Vector cd=new Vector();
            for(int i=1;i<=q;i++)
            {
                cd.add(rs.getString("p.pid"));
                cd.add(rs.getString("p.Brand"));
                cd.add(rs.getString("p.pname"));
                cd.add(rs.getString("p.QType"));
                cd.add(rs.getString("p.price"));
                cd.add(rs.getString("p.discount"));      
                cd.add(rs.getString("p.finalPrice"));
                cd.add(rs.getString("s.totalquantity"));
                //total=Float.parseFloat(rs.getString("s.totalprice"));
                cd.add(rs.getString("s.totalprice")); 
            }
            total+=Float.parseFloat(rs.getString("s.totalprice"));
            dtm.addRow(cd);
        }
        txtTotal.setText(String.valueOf(total));
        txtTotal.setEditable(false);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, "fail");
        }
        
    }
