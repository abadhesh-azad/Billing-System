    public void updataTabel()
    {
        try
        {
        Class.forName("com.mysql.jdbc.Driver");
        con=DriverManager.getConnection(url,user,pass);
        pst=con.prepareStatement("select *from product");
        rs=pst.executeQuery();
        ResultSetMetaData rsmd=rs.getMetaData();
        int q=rsmd.getColumnCount();
        DefaultTableModel dtm=(DefaultTableModel)jTable1.getModel();
        dtm.setRowCount(0);
        while(rs.next())
        {
            Vector cd=new Vector();
            for(int i=1;i<=q;i++)
            {
                cd.add(rs.getString("pid"));
                cd.add(rs.getString("Brand"));
                cd.add(rs.getString("pname"));
                cd.add(rs.getString("QType"));
                cd.add(rs.getString("price"));
                cd.add(rs.getString("discount"));      
                cd.add(rs.getString("finalPrice"));
            }
            dtm.addRow(cd);
        }
        }
        catch(Exception e)
        {
            //JOptionPane.showMessageDialog(null, user);
        }
        
    }
