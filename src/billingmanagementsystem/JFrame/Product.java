    private void txtDiscountKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtDiscountKeyPressed
        // TODO add your handling code here:
                char c = evt.getKeyChar();
                if(Character.isDigit(c))
                {
                txtDiscount.setEditable(true);
                String a = txtDiscount.getText().toString()+c;
                String l = txtPrice.getText().toString();
                float m = Float.parseFloat(a);
                float n = Float.parseFloat(l);
                txtFinalPrice.setText("" + (n-m*n/100));
                }
                else
                {
                    txtDiscount.setEditable(false);
                }
                if(c==8)
                {
                txtDiscount.setEditable(true);
                String a = txtDiscount.getText().toString();
                String l = txtPrice.getText().toString();
                float m =0;
                if(a.length()!=0)
                {
                    m=Float.parseFloat(a);
                }
                float n = Float.parseFloat(l);
                txtFinalPrice.setText("" + (n-m*n/100));
                }
    }//GEN-LAST:event_txtDiscountKeyPressed

